@Table(database = MyDatabase.class)
public class ContentBean extends BaseModel {
    @PrimaryKey(autoincrement = true)
    Long id;
    @Column
    String name;
    @Column
    String email;
    @Column
    String QQ;
    @Column
    String Git;

    public ContentBean(){

    }

    public ContentBean(String name, String email, String QQ, String Git) {
        this.name = name;
        this.email = email;
        this.QQ = QQ;
        this.Git = Git;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getQQ() {
        return QQ;
    }

    public void setQQ(String QQ) {
        this.QQ = QQ;
    }

    public String getGit() {
        return Git;
    }

    public void setGit(String git) {
        Git = git;
    }



}